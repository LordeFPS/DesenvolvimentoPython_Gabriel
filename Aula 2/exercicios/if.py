# Media >= 7 arovado
# Media < 7 reprovado

media = 7

if media >= 7:
    # identação
    # este bloco irá executar se a condição
    # media >= 7 for True (verdadeiro)
    print("Arpovado")
else:
    # irá executar se a media >= 7 for False (falso)
    print("Reprovado")
print("Oi estou fora do if")