lista   = [ "Água", "Peixe", "Golfinho", "Tubarão"]
# índice      0        1          2           3

# Recuperando elementos da lista
# lista[inicio : fim + 1 : incremento] "FATIANDO" a lista

# "Peixe" e "Golfinho"
print(lista[1:3])
# "Água" até "Golfinho"
print(lista[0:3])
# "Peixe" até "Tubarão"
print(lista[1:])




